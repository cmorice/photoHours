package photoHours;

import java.util.Date;
import java.util.List;

public class Hours {
	
	private Date startMorningBlueHour;
	private Date startMorningGoldenHour;
	private Date startDayTime;
	
	private Date startNightTime;
	private Date startEveningGoldenHour;
	private Date startEveningBlueHour;
	
	private List<String> positions;
	
	public Date getStartMorningBlueHour() {
		return startMorningBlueHour;
	}
	public void setStartMorningBlueHour(Date startMorningBlueHour) {
		this.startMorningBlueHour = startMorningBlueHour;
	}
	public Date getStartMorningGoldenHour() {
		return startMorningGoldenHour;
	}
	public void setStartMorningGoldenHour(Date startMorningGoldenHour) {
		this.startMorningGoldenHour = startMorningGoldenHour;
	}
	public Date getStartDayTime() {
		return startDayTime;
	}
	public void setStartDayTime(Date startDayTime) {
		this.startDayTime = startDayTime;
	}
	public Date getStartNightTime() {
		return startNightTime;
	}
	public void setStartNightTime(Date startNightTime) {
		this.startNightTime = startNightTime;
	}
	public Date getStartEveningGoldenHour() {
		return startEveningGoldenHour;
	}
	public void setStartEveningGoldenHour(Date startEveningGoldenHour) {
		this.startEveningGoldenHour = startEveningGoldenHour;
	}
	public Date getStartEveningBlueHour() {
		return startEveningBlueHour;
	}
	public void setStartEveningBlueHour(Date startEveningBlueHour) {
		this.startEveningBlueHour = startEveningBlueHour;
	}
	public List<String> getPositions() {
		return positions;
	}
	public void setPositions(List<String> positions) {
		this.positions = positions;
	}
	
	


}
